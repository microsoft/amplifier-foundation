# ── Helpers ───────────────────────────────────────────────────────────────────


def _make_yaml(tmp_path: Path, content: str, name: str = "test.yaml") -> Path:
    f = tmp_path / name
    f.write_text(content)
    return f


def _make_md(tmp_path: Path, content: str, name: str = "test.md") -> Path:
    f = tmp_path / name
    f.write_text(content)
    return f


def _make_agent(tmp_path: Path, agent_name: str, description: str) -> None:
    agents_dir = tmp_path / "agents"
    agents_dir.mkdir(exist_ok=True)
    (agents_dir / f"{agent_name}.md").write_text(
        f"---\nmeta:\n  name: {agent_name}\n  description: |\n    {description}\n---\nAgent body.\n"
    )


def _extract_source_hash(md: str) -> str | None:
    match = re.search(r"source_hash:\s*([0-9a-f]+)", md)
    return match.group(1) if match else None


