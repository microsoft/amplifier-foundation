# ── Test: per-request token cost ──────────────────────────────────────────────


class TestPerRequestTokenCost:
    def test_zero_cost_behavior_shows_zero(self, tmp_path: Path) -> None:
        """Pure YAML with no context/agents → cost shows ~0 tok."""
        f = _make_yaml(tmp_path, "bundle:\n  name: zero-cost\n  version: 1.0.0\n")
        result = bundle_detail_md(f)
        assert "~0 tok" in result

    def test_md_body_adds_to_cost(self, tmp_path: Path) -> None:
        """.md bundle body is counted in per-request token cost."""
        body = "This is a long instruction body. " * 20  # ~165 tokens
        f = _make_md(
            tmp_path,
            f"---\nbundle:\n  name: md-body\n  version: 1.0.0\n---\n{body}",
        )
        result = bundle_detail_md(f, repo_root=tmp_path)
        # Token cost must be > 0 and contain "tok"
        match = re.search(r"Per-request main-session token cost: ~(\d+) tok", result)
        assert match is not None, f"cost line not found in:\n{result[:500]}"
        cost = int(match.group(1))
        assert cost > 0, "Expected non-zero cost for md bundle with body"

    def test_agent_description_contributes_to_cost(self, tmp_path: Path) -> None:
        """Agent description tokens contribute to per-request cost."""
        description = "A useful agent. " * 20  # non-trivial token count
        _make_agent(tmp_path, "my-agent", description)
        f = _make_yaml(
            tmp_path,
            "bundle:\n  name: agent-cost\n  version: 1.0.0\n"
            "agents:\n  include:\n    - foundation:my-agent\n",
        )
        result = bundle_detail_md(f, repo_root=tmp_path)
        match = re.search(r"Per-request main-session token cost: ~(\d+) tok", result)
        assert match is not None
        cost = int(match.group(1))
        assert cost > 0


