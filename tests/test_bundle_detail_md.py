# ── Parametrized: all real behavior files ─────────────────────────────────────

_BEHAVIOR_FILES = sorted(
    glob.glob(str(Path(__file__).parent.parent / "behaviors" / "*.yaml"))
)


class TestAllRealBehaviors:
    """Parametrized tests over all real behavior files."""

    @pytest.mark.parametrize(
        "behavior_path",
        _BEHAVIOR_FILES,
        ids=[Path(p).stem for p in _BEHAVIOR_FILES],
    )
    def test_behavior_generates_valid_markdown(self, behavior_path: str) -> None:
        """Every behavior file generates valid markdown with header and footer."""
        repo = Path(__file__).parent.parent
        result = bundle_detail_md(behavior_path, repo_root=repo)
        assert isinstance(result, str)
        assert len(result) > 0
        # Must have H1 header
        assert result.startswith("#"), f"Expected markdown H1 at start:\n{result[:100]}"
        # Must have source_hash in footer
        assert "source_hash:" in result, "Missing source_hash in footer"
        # Must have all section headers
        assert "## Tools" in result
        assert "## Context Files" in result
        assert "## Agents" in result
        assert "## Hooks" in result
        assert "## Skills" in result
