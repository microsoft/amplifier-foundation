# ── Test: source_hash ─────────────────────────────────────────────────────────


class TestSourceHash:
    def test_source_hash_present_in_footer(self, tmp_path: Path) -> None:
        """Footer contains 'source_hash:' label."""
        f = _make_yaml(tmp_path, "bundle:\n  name: hash-test\n  version: 1.0.0\n")
        result = bundle_detail_md(f)
        assert "source_hash:" in result

    def test_source_hash_is_hex(self, tmp_path: Path) -> None:
        """source_hash value looks like a SHA-256 hex digest."""
        f = _make_yaml(tmp_path, "bundle:\n  name: hash-hex\n  version: 1.0.0\n")
        result = bundle_detail_md(f)
        h = _extract_source_hash(result)
        assert h is not None, f"source_hash not found in:\n{result[-200:]}"
        assert re.fullmatch(r"[0-9a-f]+", h), f"Not a hex string: {h!r}"
        assert len(h) == 64, f"Expected SHA-256 (64 hex chars), got {len(h)}"

    def test_source_hash_is_deterministic(self, tmp_path: Path) -> None:
        """Same file content → same source_hash on repeated calls."""
        f = _make_yaml(tmp_path, "bundle:\n  name: stable-hash\n  version: 1.0.0\n")
        r1 = bundle_detail_md(f)
        r2 = bundle_detail_md(f)
        h1 = _extract_source_hash(r1)
        h2 = _extract_source_hash(r2)
        assert h1 == h2, "source_hash should be deterministic for the same file"

    def test_source_hash_changes_with_content(self, tmp_path: Path) -> None:
        """Different file content → different source_hash."""
        f1 = _make_yaml(
            tmp_path, "bundle:\n  name: beh-a\n  version: 1.0.0\n", "a.yaml"
        )
        f2 = _make_yaml(
            tmp_path, "bundle:\n  name: beh-b\n  version: 1.0.0\n", "b.yaml"
        )
        h1 = _extract_source_hash(bundle_detail_md(f1))
        h2 = _extract_source_hash(bundle_detail_md(f2))
        assert h1 != h2, "Different file content should produce different source_hash"


